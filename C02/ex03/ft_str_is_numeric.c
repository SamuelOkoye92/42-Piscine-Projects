#include <string.h>
#include <unistd.h>

int	ft_str_is_numeric(char *str)
{
	int	d;

	d = 0;
	while (str[d] != '\0')
	{
		if (!(str[d] >= '0' && str[d] <= '9'))
		{
			return (0);
		}
		d++;
	}
	return (1);
}
