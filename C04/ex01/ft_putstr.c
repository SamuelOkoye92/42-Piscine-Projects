#include <unistd.h>
#include <string.h>

void	ft_str(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int	c;

	c = 0;
	while (str[c] != '\0')
	{
		ft_str(str[c]);
		c++;
	}
}
